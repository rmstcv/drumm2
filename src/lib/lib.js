function audio () {

    const audioFiles = {};

    function addAudio () {
        soundsBase.forEach(item => {
            audioFiles[item.name] = [];
            let numb;
            for (let i = 1; i <= item.base.length; i++) {
                if (i<10) {
                    numb = `-0${i}`;
                } else {
                    numb = `-${i}`;
                }
                audioFiles[item.name].push({name: item.name+`${numb}`, sound: item.base[i-1]});
            }
        });

    };

    addAudio();

    return audioFiles;
}

